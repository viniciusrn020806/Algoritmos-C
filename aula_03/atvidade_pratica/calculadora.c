float soma(float num1, float num2){
    return num1 + num2;
}

float sub(float num1, float num2){
    return num1 - num2;
}

float div(float num1,float num2){
    if (num2 != 0){
        return num1 / num2;       
    } else {
      return 0;
}
}

float multi(float num1,float num2){
    return num1 * num2;
}
